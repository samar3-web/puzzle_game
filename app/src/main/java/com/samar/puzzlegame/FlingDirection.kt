package com.samar.puzzlegame

enum class FlingDirection {
    /**
     * Upward fling direction.
     */
    UP,

    /**
     * Downward fling direction.
     */
    DOWN,

    /**
     * Left fling direction.
     */
    LEFT,

    /**
     * Right fling direction.
     */
    RIGHT,

    /**
     * Unrecognized fling direction.
     */
    INVALID
}